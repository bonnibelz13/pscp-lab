score = 50


if score >= 80:         #ถ้าคะแนนมากกว่าหรือเท่ากับ 80 จะได้เกรด4
    print('เกรด4')

elif score >= 75:       #ถ้าคะแนนมากกว่าหรือเท่ากับ 75 จะได้เกรด3.5
    print('เกรด3.5')

else:                   #ถ้าคะแนนนอกเหนือจาก80 กับ 75 จะได้เกรด3
    print('เกรด3')
